package ejercicio4xg6.Entidades;

/**
 *
 * @author TOMI
 */
public class Entidades {
    private int Dni;
    private String Letra;
    
    
    public Entidades () {
        
    }
    
    public Entidades(int Dni, String Letra ) {
        this.Dni = Dni;
        this.Dni = Dni;
        
    }

    public int getDni() {
        return Dni;
    }

    public void setDni(int Dni) {
        this.Dni = Dni;
    }

    public String getLetra() {
        return Letra;
    }

    public void setLetra(String Letra) {
        this.Letra = Letra;
    }
    
    
}
